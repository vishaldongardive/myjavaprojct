package loopPracties;

public class Twotable_ForLoop {

	public static void main(String[] args) 
	{  int count=1;
	
		for (int a=2; a<=20; a=a+2)
		{
			System.out.println("2"+count+"-"+a);
			count++;
		}
		
		System.out.println("");
		for (int a=2; a<=20; a=a+2)
		{
			System.out.println(a);
		}
	
	}

}
	



